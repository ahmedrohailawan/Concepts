#include <iostream>
using namespace std;
int main()
{
	int a;
	cout<<"size of int "<<sizeof(a)<<endl;
	float b;	
	cout<<"size of float "<<sizeof(b)<<endl;
	char c;
	cout<<"size of char "<<sizeof(c)<<endl;
	bool d;
	cout<<"size of bool "<<sizeof(d)<<endl;		
	
	//type modifiers
	
	short int e;
	cout<<"size of short int  "<<sizeof(d)<<endl;	
	long int f;
	cout<<"size of long int  "<<sizeof(d)<<endl;			 	
}
